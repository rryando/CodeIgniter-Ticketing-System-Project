			<p>
			
	<p>	Dalam upaya meningkatkan mutu dan kualitas hasil didik dalam program belajar mengajar di Telkom University, maka Telkom Foundation memberlakukan kebijakan mewajibkan bagi seluruh mahasiswa/i baru tinggal di Asrama yang telah disediakan untuk masa 1 (satu) tahun pertama yang bertujuan dalam rangka membentuk karakter mahasiswa dengan pemberian pembinaan dasar-dasar kepribadian yang didasarkan pada semangat kebersamaan, nasionalisme, iman, dan taqwa </p>

		<p> I. Dasar Pertimbangan

		Meningkatkan  mutu dan  kualitas  hasil didik bagi  mahasiswa/i baru Telkom University dalam naungan Telkom Foundation. </p>

		<p> II. Tujuan

		Membentuk mahasiswa yang tangguh dan trengginas yang didukung dengan kepribadian dan kemampuan bekerja berlandaskan intelektual, berjiwa nasionalis Indonesia, iman dan taqwa.
		
		</p>
		
		<p>
		III. Lokasi Asrama

		Asrama terletak dalam 1 (satu) kawasan Telkom University, yang dipisahkan antara putra dan putri di jalan Telekomunikasi No. 1 Bandung Technoplex, terusan Buah Batu, Kabupaten Bandung.
		
		</p>
		
		<p>

		IV. Fasilitas Hunian

		Terdiri dari 8 (delapan) unit gedung untuk Asrama Putra dan 8 (delapan) unit gedung untuk Asrama Putri.
		Masing-masing Gedung Asrama terdiri dari 4 (empat) lantai, dengan 94 unit kamar, masing-masing kamar seluas 23 M2 yang dilengkapi dengan 1 (satu) unit kamar mandi ANC;
		Masing-masing kamar dilengkapi dengan 2 (dua) unit tempat tidur bertingkat untuk 4 (empat) orang penghuni.
		Masing-masing penghuni mendapatkan 1 (satu) tempat tidur lengkap dengan kasur, bantal, guling, sprei dan Meja belajar, lemari pakaian, kursi belajar dan rak buku.
		Masing-masing kamar dilengkapi dengan 1 (satu) tempat gantungan handuk, gantungan baju, cermin, ember besar, gayung air, tempat sampah, keset dan 1 (satu) buah Dispenser(biaya penggunaan air gallon menjadi tanggung jawab penghuni).
		Setiap lantai gedung tersedia ruang lobby bersama dengan fasilitas Televisi dan di lantai dasar tersedia 1 (satu) ruang sholat, Televisi dan 1 (satu) dapur bersih.
		Penggunaan air bersih free dan biaya listrik ditanggung penghuni dengan sistem pulsa listrik prabayar (token) untuk masing-masing kamar.
	
		</p>
		
		<p>
		
		V. Fasilitas Pendukung Lainnya	

		Akses Point Internet / WIFI tersedia dan bila penghuni menginginkan akses Internet dengan kecepatan    (bandwith) tinggi dan yang lebih cepat pihak pengelola  asrama  menyediakan  fasilitas pulsa Internet dengan sistem berbayar/ pulsa.
		Tersedia tempat parkir khusus sepeda, dan pengelola menyediakan sepeda dengan jumlah terbatas yang dapat dipinjam secara bergilir.
		Kantin-kantin dan  laundry pembayaran tanggung jawab masing-masing penghuni.
		Lapangan Futsal dan Lapangan Volly.
		Sistem pengamanan dilakukan   24 Jam balk dalam maupun di lingkungan asrama.
		Tersedia Poliklinik yang terletak dalam kawasan Telkom University dengan fasilitas mobil Ambulance yang slap 24 jam.
		Disetiap lantai dasar gedung asrama ditempat 1 (satu) orang petugas helpdesk dan tenaga Cleaning Service.
		Tersedia area yang dapat dipergunakan untuk dipakai sebagai tempat pelaksanaan senan kebugaran.

		</p>
		
		<p>
		VI. Kegiatan Softskill

		Guna mencapai tujuan terbentuknya mahasiswa yang tangguh dan trenginas, yang  memiliki kepribadian dan berkemampuan bekerja berlandaskan intelektual, berjiwa nasionalis Indonesia, iman dan taqwa, maka penghuni asrama diberikan kegiatan-kegiatan  softskill yang  dilaksanakan  oleh  Unit khusus di Telkom University dalam bentuk seminar atau pelatihan-pelatihan yang   dilaksanakan selama masa waktu tinggal di asrama. Dalam beberapa kesempatan kegiatan tersebut juga dapat dilakukan berbentuk permainan maupun kegiatan hari sehat, lingkungan bersih yang dilaksanakan pada akhir pekan di bulan-bulan tertentu.
		
		</p>
		
		
		<p>

		VII. Kampus dan Asrama ramah lingkungan (Go Green)

		Dalam rangka penciptaan lingkungan Kampus dan Asrama yang ramah lingkungan (GO  Green)  kepada seluruh  mahasiswa  baru agar tidak membawa dan mempergunakan kedaraan bermotor dalam kawasan Telkom University.
		Dalam  lingkungan asrama tidak tersedia  lahan parkir untuk kendaraan bermotor, dan hanya tersedia di masing-masing Fakultas atau kampus.
		Asrama hanya menyediakan lahan parkir yang dikhususkan untuk sepeda, Mahasiswa dihimbau agar mempergunakan alat transfortasi dari asrama ke kampus menggunakan sepeda atau berjalan.
		Asrama menyediakan sepeda dengan jumlah terbatas, yang dapat dipinjam oleh mahasiswa dengan cara bergiliran sebagai alat transfortasi dari Asrama ke Kampus atau sebaliknya.
	
	
		</p>
		
		<p>
		VIII. Senior Residence (SR)

		Guna terciptanya kenyamanan dan keamanan hunian bagi para mahasiswa baru di Asrama, Telkom University menempatkan mahasiswa senior dimasing-masing gedung minimal 4 (empat) orang yang ditugaskan untuk membantu, membimbing, mengarahkan dan mengawasi kegiatan-kegiatan para mahasiswa penghuni di asrama.
		
		
		</p>
		
		<p>
		IX. Biaya Asrama

		Biaya sewa sebesar Rp. 2.500.000,- /tahun, dibayarkan bersamaan dengan pelunasan biaya SDP2.
		
		</p>

		
		<p>
		X. Waktu Penempatan Asrama

		Alokasi penempatan asrama akan dilakukan sebagai berikut :

		Penempatan para mahasiswa di asrama dipisah antara Putra dan Putri.
		Penempatan dalam 1 (satu) kamar diupayakan 1 (satu) jurusan yang tentukan oleh Pihak Pengelola Asrama baik Putra maupun Putri.
		Mahasiswa/i sudah dapat menempati asrama 5 (lima) hari sebelum dimulai kegiatan perkuliahan.
		Tanggal berakhirnya menempati asrama sampai dengan tanggal tiga puluh bulan Juni tahun dua ribu lima belas (30-06-2015).
		</p>
			
			
